func yarnDep(coordinates, version, alg, digest string) build.Dependency {
	return build.Dependency{
		Coordinates: coordinates,
		Version:     version,
		CheckSum: build.CheckSum{
			Algorithm: alg,
			Digest:    digest,
		},
	}
}

